poem = ( "山裡有山路轉彎高山流水響潺潺"
		 "深山百鳥聲聲叫路上行人步步難"
		 "勸君莫作雲遊客孤身日日在山間"
		 "人人說道華山好我道華山第八山" )

# p1:左半部詩句	  p2:右半部詩句
p1 , p2 = poem[-28:][::-1] , poem[:28]

# 由上而下，各列半邊詩句字數
ns = [1,1,2,2,3,3,4,4,4,4]

a = 0
for i in range(len(ns)) :

	b = a + ns[i]

	if i%2 :
		print( "  "*(4-ns[i]) + p1[a:b] + p2[a:b][::-1] )
	else :
		print( "  "*(4-ns[i]) + p1[a:b][::-1] + p2[a:b] )

	a = b
